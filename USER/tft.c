void TFT_WriteCom(u8 commport) {
	TFT_RS_CLR;
	TFT_SET(commport);
	TFT_WR_CLR;
	TFT_WR_SET;
	TFT_RS_SET;
}

void TFT_WriteData(u8 data) {
 TFT_RS_SET;
 TFT_SET(data);
 TFT_WR_CLR;
 TFT_WR_SET;
}

// Clear screen
void TFT_Cls(void) {
 TFT_Fill(ScreenX0, ScreenY0, ScreenXsize, ScreenYsize, clBlack);
}

void TFT_SetWindow(u16 x, u16 xsize, u16 y, u16 ysize) {
 TFT_WriteCom(0x2A);
 TFT_WriteData(x >> 8);
 TFT_WriteData(x);
 x = x + xsize - 1;
 TFT_WriteData(x >> 8);
 TFT_WriteData(x);

 TFT_WriteCom(0x2B);
 TFT_WriteData(y >> 8);
 TFT_WriteData(y);
 y = y + ysize - 1;
 TFT_WriteData(y >> 8);
 TFT_WriteData(y);
}

// ==========================================
// Fill rectangle area with given color
void TFT_Fill(s16 x, s16 y, s16 xsize, s16 ysize, u16 color) {
	u32 tmp; 
	TFT_SetWindow(x, xsize, y, ysize);
	TFT_WR2C;
	tmp = (u32)xsize * (u32)ysize;
	while(tmp--) {
		TFT_SET((color >> 8));
		TFT_WR_CLR;
		TFT_WR_SET;
		TFT_SET(color);
		TFT_WR_CLR;
		TFT_WR_SET;
	}
}

// Put at (x, y) a 15X16 character [ch] with [f_color] and [b_color]
void TFT_WriteChar(u16 x, u16 y, u8 ch, u16 fgcolor, u16 bgcolor) {
	u8 tmp, tmp2;
	u16 tmp1, tmp3;
	u8 *ptmp;
	// Font address
	ptmp = (u8 *)Font_ASC8X16 + (ch - 0x20) * 16;
	TFT_SetWindow(x, 8, y, 16);
	TFT_WR2C;
	tmp1 = (8 * 16)/8;
	while(tmp1) {
		tmp = *ptmp;
		tmp2 = 8;
		while(tmp2) {
			tmp3 = (tmp & 0x80) ? fgcolor : bgcolor;
			TFT_SET((tmp3 >> 8));
			TFT_WR_CLR;
			TFT_WR_SET;
			TFT_SET(tmp3);
			TFT_WR_CLR;
			TFT_WR_SET;
			tmp <<= 1;
			tmp2--;
		}
		ptmp++;
		tmp1--;	
	}
}

void TFT_WriteString(u16 x, u16 y, u8 *str, u16 fgcolor, u16 bgcolor) {
	u8 tmp;
	//while((tmp = *str++)) {
	for (tmp = *str; tmp != '\0'; tmp = *++str) {
		TFT_WriteChar(x, y, tmp, fgcolor, bgcolor);
		x += 8;
		if(x >= ScreenXsize) {
			y += 16;
			x = 0;
		}
	}
}

